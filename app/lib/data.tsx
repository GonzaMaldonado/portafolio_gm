export async function fetchCardData() {
  noStore();

  try {
    const skillsCountPromise = sql`SELECT COUNT(*) FROM skills`;
    const projectsCountPromise = sql`SELECT COUNT(*) FROM projects`;
  

    const data = await Promise.all([
      skillsCountPromise,
      projectsCountPromise,
    ]);

    const numberOfSkills = Number(data[0].rows[0].count ?? '0');
    const numberOfProjects = Number(data[1].rows[0].count ?? '0');

    return {
      numberOfSkills,
      numberOfProjects,
    };
  } catch (error) {
    console.error('Database Error:', error);
    throw new Error('Failed to fetch card data.');
  }
}

